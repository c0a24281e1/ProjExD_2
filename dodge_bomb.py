def main():
    pg.display.set_caption("逃げろ！こうかとん")
    screen = pg.display.set_mode((WIDTH, HEIGHT))
    bg_img = pg.image.load("fig/pg_bg.jpg")    
    kk_img = pg.transform.rotozoom(pg.image.load("fig/3.png"), 0, 0.9)
    kk_rct = kk_img.get_rect()
    kk_rct.center = 300, 200
    bb_img = pg.Surface((20,20))  #空のSurface
    pg.draw.circle(bb_img,(255,0,0),(10,10),10)  #半径10の赤い円を描画
    bb_img.set_colorkey((0,0,0))  #黒色を透明に設定
    bb_rct = bb_img.get_rect()  #爆弾Rect
    bb_rct.center = random.randint(0,WIDTH),random.randint(0,HEIGHT)  #爆弾座標
    vx, vy = +5, +5  #爆弾の横速度、縦速度
    bb_img
    clock = pg.time.Clock()
    tmr = 0
    
    while True:
        for event in pg.event.get():
            if event.type == pg.QUIT: 
                return
        if kk_rct.colliderect(bb_rct):  #こうかとんが爆発したら
            # print("ゲームオーバー")
            gameover(screen)

            return
        screen.blit(bg_img, [0, 0]) 
        key_lst = pg.key.get_pressed()
        sum_mv = [0, 0]
        # if key_lst[pg.K_UP]:
        #     sum_mv[1] -= 5
        # if key_lst[pg.K_DOWN]:
        #     sum_mv[1] += 5
        # if key_lst[pg.K_LEFT]:
        #     sum_mv[0] -= 5
        # if key_lst[pg.K_RIGHT]:
        #     sum_mv[0] += 5
        for key,mv in DELTA.items():
            if key_lst[key]:
                sum_mv[0] += mv[0]  #横方向の移動量
                sum_mv[1] += mv[1]  #縦方向の移動量

        kk_rct.move_ip(sum_mv)
        if check_bound(kk_rct) != (True, True):  #画面外なら
            kk_rct.move_ip(-sum_mv[0], -sum_mv[1])  #移動をなかったことにする
        screen.blit(kk_img, kk_rct)
        bb_rct.move_ip(vx, vy)
        yoko, tate = check_bound(bb_rct)
        if not yoko:  #横方向にはみ出ていたら
            vx *= -1
        if not tate:
            vy *= -1
        screen.blit(bb_img, bb_rct)
        pg.display.update()
        tmr += 1
        clock.tick(50)


if __name__ == "__main__":
    pg.init()
    main()
    pg.quit()
    sys.exit()
